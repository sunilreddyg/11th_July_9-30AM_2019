package corejava.Loops;

public class Foreach {

	public static void main(String[] args) 
	{
		
		
		//Example:--> Print array values.
		String tools[]= {"IDE","RC","WD","Appium","Cucumber"};
		//Apply foreach loop to iterate for arrange range
		for (String eachtool : tools) 
		{
			System.out.println(eachtool);
		}
		
		
		//Example:--> Print Numeric values
		int num[]= {100,200,300,400,500,600};
		//Iterate for number of array times
		for (int i : num) 
		{
			System.out.println(i);
		}
		
		
		
		
		
		
		
		
		
;
	}

}
